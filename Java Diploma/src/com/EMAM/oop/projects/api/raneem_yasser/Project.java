package com.EMAM.oop.projects.api;

public class Project {

    public static void main(String[] args) {
        int[] a = {12, 13, 4, 6, 8, 3, 8};
        System.out.println(Math.PI);
        System.out.println(Math.sumArray(a));
        System.out.println(Math.permutations(20, 2));
        System.out.println(Math.Combination(2, 3));
        System.out.println(Math.FirstDegreeEquation(3, 6));
        System.out.println(Math.LastCommonFactor(12, 6));
        System.out.println(Math.StandardMultiplication(a));
        System.out.println(Math.addition(4, 3));
        System.out.println(Math.division(16, 4));
        System.out.println(Math.factorial(7));
        System.out.println(Math.multiplication(6, 8));
        System.out.println(Math.power(7, 2));
        System.out.println(Math.subtraction(7, 6));
    }

}
